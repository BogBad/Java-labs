// Letter.java
public class Letter {
    private char character;

    /**
     * Конструктор для букви.
     * @param character Символ букви.
     */
    public Letter(char character) {
        this.character = character;
    }

    /**
     * Отримати символ букви.
     * @return Символ.
     */
    public char getCharacter() {
        return character;
    }
}
